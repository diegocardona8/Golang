package main

import "fmt"

func main() {

	a := 20
	b := 10

	//Sum
	//:= Declare and assign variable
	result := a + b
	fmt.Println("Sum: ", result)

	//Rest

	result = a - b
	fmt.Println("Rest: ", result)

	//Multiply

	result = a * b
	fmt.Println("Multiply: ", result)

	//Split Variable of type float

	divi := 3.0 / 2.0
	fmt.Println("Split: ", divi)

	//Module
	//:= Assign variable
	result = 3 % 2
	fmt.Println("Module:", result)
}
